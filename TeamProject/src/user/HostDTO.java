package user;

public class HostDTO {
	
	private String host_id;
	private String email;
	private String host_pass;
	private String host_nic;
	private String host_phone;
	private int host_level;
	private int point;
	
	// getter, setter
	public String getHost_id() {
		return host_id;
	}
	public void setHost_id(String host_id) {
		this.host_id = host_id;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}	
	public String getHost_pass() {
		return host_pass;
	}
	public void setHost_pass(String host_pass) {
		this.host_pass = host_pass;
	}
	public String getHost_nic() {
		return host_nic;
	}
	public void setHost_nic(String host_nic) {
		this.host_nic = host_nic;
	}
	public String getHost_phone() {
		return host_phone;
	}
	public void setHost_phone(String host_phone) {
		this.host_phone = host_phone;
	}
	public int getHost_level() {
		return host_level;
	}
	public void setHost_level(int host_level) {
		this.host_level = host_level;
	}
	public int getPoint() {
		return point;
	}
	public void setPoint(int point) {
		this.point = point;
	}
	
	
	
	
	
	
	
}

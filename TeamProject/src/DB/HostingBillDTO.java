package DB;

public class HostingBillDTO {
	
	private String room_no;
	private String room_day;
	private int room_sum;
	
	public String getRoom_no() {
		return room_no;
	}
	public void setRoom_no(String room_no) {
		this.room_no = room_no;
	}
	public String getRoom_day() {
		return room_day;
	}
	public void setRoom_day(String room_day) {
		this.room_day = room_day;
	}
	public int getRoom_sum() {
		return room_sum;
	}
	public void setRoom_sum(int room_sum) {
		this.room_sum = room_sum;
	}
	
	
	
	

}
